#include <iostream>
using namespace std;

int main () {
	
	//Kalkulator sederhana berdasarkan angka diinputkan oleh user
	int a, b;
	
	//input
	cout<<"Masukan Angka Pertama: ";cin>>a;
	cout<<"masukan Angka Kedua  : ";cin>>b;
	
	
	//output
	cout<<"======HASIL OPERATOR MATEMATIKA======"<<endl;
	cout<<"Hasil Penjumlahan : " << a+b <<endl; 
	cout<<"Hasil Pengurangan : " << a-b <<endl;
	cout<<"Hasil Perkalian   : " << a*b <<endl;
	cout<<"Hasil Pembagian   : " << a/b <<endl;
	cout<<"Hasil Modulus     : " << a%b <<endl; 
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
return 0;
}
